package Pages;

public class Address {
    private String state;
    private String city;
    private Add add;

    public String getState() {
        return state;
    }
    public void setState(String state) {
        this.state = state;
    }
    public String getCity() {
        return city;
    }
    public void setCity(String city) {
        this.city = city;
    }
    public Add getAdd() {
        return add;
    }
    public void setAdd(Add add) {
        this.add = add;
    }
}
